
def Calc_Area(w,h): #Create a function using Parameters
    Area = w * h
    #print("Area is: ", Area) # Print used to print the output value after running the function
    return Area # Return used to store the output value after running the function

Calc_Area(20,30) # Call the function using arguments

result = Calc_Area(20,30) # Store the function output value in a variable
print("Area is: ", result) # Print the output value stored in the variable

